import { DocumentFormValidationContext, Section, DocumentFormManagerBase } from 'eau-documents'
import { ResourceHelpers } from 'eau-core';
import { CertificateForAccidentVM } from '../models/ModelsAutoGenerated';
import { CertificateForAccidentValidator } from '../validations/forms/CertificateForAccidentValidator';
import { CertificateForAccidentUI } from '../ui/certificate-forms/CertificateForAccidentUI';


export class CertificateForAccidentManager extends DocumentFormManagerBase<CertificateForAccidentVM>{

    //#region ApplicationFormManagerBase

    createDocument(obj: any): CertificateForAccidentVM {
        return new CertificateForAccidentVM(obj);
    }

    //#endregion   

    protected createSections(validationContext: DocumentFormValidationContext): Section[] {
        var sections = super.createSections(validationContext);

        var main = new Section();
        main.code = "main";
        main.title = ResourceHelpers.getResourceByProperty(m => m, CertificateForAccidentVM);
        main.form = this.documentForm;
        main.formUICmp = CertificateForAccidentUI;
        main.validator = new CertificateForAccidentValidator();
        main.validator.setValidationContext(validationContext);
        main.validate = () => this.validateSection(main);

        sections.push(main);

        return sections;
    }
}