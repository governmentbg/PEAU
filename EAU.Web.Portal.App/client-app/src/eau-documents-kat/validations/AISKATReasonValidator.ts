import { EAUBaseValidator, ResourceHelpers, ErrMsgCodesConstants } from "eau-core";
import { DocumentFormValidationContext } from "eau-documents";
import { AISKATReason } from "../models/ModelsAutoGenerated";

export class AISKATReasonValidator extends EAUBaseValidator<AISKATReason, DocumentFormValidationContext> {

    constructor(resourceProperty?: string) {
        super();

        this.ruleFor(m => m.code).notEmpty()
            .withMessage(ResourceHelpers.formatErrorMessage(ErrMsgCodesConstants.DefaultNotEmptyErrorMessage, new AISKATReason(), resourceProperty ? resourceProperty : 'code'));

        this.ruleFor(m => m.name).notEmpty()
            .withMessage(ResourceHelpers.formatErrorMessage(ErrMsgCodesConstants.DefaultNotEmptyErrorMessage, new AISKATReason(), resourceProperty ? resourceProperty : 'name'));
    }
}