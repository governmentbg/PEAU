import { BaseProps } from "cnsys-ui-react";
import { EAUBaseComponent } from "eau-core";
import React from "react";
import { ReportForVehicleEUCARISData } from "../../models/ModelsAutoGenerated";
import { DisplayStatusesUI } from "./DisplayStatusesUI";

export class EucarisDataUI extends EAUBaseComponent<BaseProps, ReportForVehicleEUCARISData> {

    render(): JSX.Element {
        return (
            <>
                {
                    this.model.colorName || this.model.additionalInfo
                        ? <div className="row">
                            {
                                this.model.colorName && <div className="form-group col-sm-4">
                                    <h4 className="form-control-label">{this.getResourceByProperty(x => x.colorName)}</h4>
                                    <p className="field-text">{this.model.colorName}</p>
                                </div>
                            }
                            {
                                this.model.additionalInfo && <div className="form-group col-sm-8">
                                    <h4 className="form-control-label">{this.getResourceByProperty(x => x.additionalInfo)}</h4>
                                    <p className="field-text">{this.model.additionalInfo}</p>
                                </div>
                            }
                        </div>
                        : null
                }
                {
                    this.model.canUseCertificateForRegistration
                        ? <div className="alert alert-success">
                            <p>{this.getResource("DOC_KAT_CERTIFICATE_FOR_REGISTRATION_INFO_L")}</p>
                        </div>
                        : null
                }
                <DisplayStatusesUI statuses={this.model.statuses} />
            </>
        );
    }
}