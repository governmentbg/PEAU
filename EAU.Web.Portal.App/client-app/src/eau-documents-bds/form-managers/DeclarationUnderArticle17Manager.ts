import { ResourceHelpers } from 'eau-core';
import { ApplicationFormManagerBase, DocumentFormValidationContext, Section } from 'eau-documents';
import { action } from 'mobx';
import { DeclarationUndurArticle17VM, IssuingBgPersonalDocumentReasonData } from '../models/ModelsAutoGenerated';
import { DeclarationUnderArticle17DataUI } from '../ui/section-forms/DeclarationUnderArticle17DataUI';
import { DeclarationUnderArticle17DataValidator } from '../validations/DeclarationUnderArticle17DataValidator';
import { DeclarationUnderArticle17ApplicantReceiptUI } from '../ui/section-forms/DeclarationUnderArticle17ApplicantReceiptUI';

export class DeclarationUnderArticle17Manager extends ApplicationFormManagerBase<DeclarationUndurArticle17VM> {

    //#region ApplicationFormManagerBase

    protected createDocument(obj: any): DeclarationUndurArticle17VM {
        return new DeclarationUndurArticle17VM(obj);
    }

    //#endregion

    @action protected initDocumentForm() {
        super.initDocumentForm();

        if (!this.documentForm.circumstances.reasonData)
            this.documentForm.circumstances.reasonData = new IssuingBgPersonalDocumentReasonData();
    }

    protected createSections(validationContext: DocumentFormValidationContext): Section[] {
        var sections = super.createSections(validationContext);

        sections[sections.findIndex(el => el.code == 'wayOfDelivery')].formUICmp = DeclarationUnderArticle17ApplicantReceiptUI;

        var circumstances = new Section();
        circumstances.code = "circumstances";
        circumstances.title = ResourceHelpers.getResourceByProperty(m => m.circumstances, this.documentForm);
        circumstances.form = this.documentForm.circumstances;
        circumstances.formUICmp = DeclarationUnderArticle17DataUI;
        circumstances.validator = new DeclarationUnderArticle17DataValidator();
        circumstances.validator.setValidationContext(validationContext);
        circumstances.validate = () => this.validateSection(circumstances);

        sections.splice(1, 0, circumstances);

        return sections;
    }
}