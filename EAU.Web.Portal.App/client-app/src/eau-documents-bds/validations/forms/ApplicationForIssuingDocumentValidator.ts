import { EAUBaseValidator } from "eau-core";
import { DeclarationsVMValidator, DocumentFormValidationContext, ElectronicServiceApplicantVMValidator, PersonalInformationValidator, ServiceTermTypeAndApplicantReceiptVMValidator } from "eau-documents";
import { ApplicationForIssuingDocumentVM } from "../../models/ModelsAutoGenerated";
import { ApplicationForIssuingDocumentDataValidator } from "../ApplicationForIssuingDocumentDataValidator";

export class ApplicationForIssuingDocumentValidator extends EAUBaseValidator<ApplicationForIssuingDocumentVM, DocumentFormValidationContext> {

    constructor() {
        super();

        this.ruleFor(m => m.electronicServiceApplicant).setValidator(new ElectronicServiceApplicantVMValidator());
        this.ruleFor(m => m.circumstances).setValidator(new ApplicationForIssuingDocumentDataValidator());
        this.ruleFor(m => m.personalInformation).setValidator(new PersonalInformationValidator(true));
        this.ruleFor(m => m.declarations).setValidator(new DeclarationsVMValidator());
        this.ruleFor(m => m.serviceTermTypeAndApplicantReceipt).setValidator(new ServiceTermTypeAndApplicantReceiptVMValidator());
    }
}