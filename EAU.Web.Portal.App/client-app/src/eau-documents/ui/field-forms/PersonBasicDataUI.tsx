import { BaseProps } from "cnsys-ui-react";
import { EAUBaseComponent } from "eau-core";
import { observer } from "mobx-react";
import React from "react";
import { PersonBasicDataVM } from "../../models/ModelsAutoGenerated";
import { IdentityDocumentBasicDataUI } from "./IdentityDocumentBasicDataUI";
import { PersonIdentifierUI } from "./PersonIdentifierUI";
import { PersonNamesUI } from "./PersonNamesUI";

interface PersonBasicDataUIProps extends BaseProps {
    skipIdentityDocumentBasicData?: boolean;
}

@observer export class PersonBasicDataUI extends EAUBaseComponent<PersonBasicDataUIProps, PersonBasicDataVM> {

    renderEdit(): JSX.Element {
        return (
            <>
                <PersonNamesUI {...this.bind(m => m.names)} />
                <PersonIdentifierUI {...this.bind(m => m.identifier)} />
                {
                    this.props.skipIdentityDocumentBasicData == true
                        ? null
                        : <IdentityDocumentBasicDataUI {...this.bind(m => m.identityDocument)} />
                }
            </>
        );
    }

    renderDisplay(): JSX.Element {
        return (
            <>
                <div className="row">
                    <PersonNamesUI {...this.bind(m => m.names)} />
                    <PersonIdentifierUI {...this.bind(m => m.identifier)} />
                </div>
                {
                    this.props.skipIdentityDocumentBasicData == true
                        ? null
                        : <IdentityDocumentBasicDataUI {...this.bind(m => m.identityDocument)} />
                }
            </>
        );
    }
}